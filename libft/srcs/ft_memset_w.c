#include "libft.h"

void	*ft_memset_w(void *b, int c, size_t len)
{
	size_t			i;
	wchar_t			*x;

	i = 0;
	x = (wchar_t *)b;
	while (i < len)
	{
		x[i] = c;
		i++;
	}
	return (x);
}
