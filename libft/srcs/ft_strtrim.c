#include "libft.h"

char	*ft_strtrim(char const *s)
{
	size_t	i;

	i = 0;
	if (!s)
		return (NULL);
	while (s[i])
		i++;
	while (*s && (s[0] == ' ' || s[0] == '\n' || s[0] == '\t'))
		s++;
	i = ft_strlen(s);
	while (i > 0 && (s[i - 1] == ' ' || s[i - 1] == '\n' || s[i - 1] == '\t'))
		i--;
	return (ft_strsub(s, 0, i));
}
