#include "libft.h"

long double		ft_sqrt(long double nb)
{
	long double		x;

	x = 1;
	if (nb > 2147483647 || nb <= 0)
		return (0);
	while (x * x < nb && x * x > 0)
		x = x + 0.000001;
	if (nb - (x - 0.000001) * (x - 0.000001) <= (x * x) - nb)
		return (x - 0.000001);
	else
		return (x);
}
